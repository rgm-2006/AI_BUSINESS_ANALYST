def send_email_gmail(to_email, company_name):
    """Sends chat summary email through Gmail."""
    try:
        import smtplib
        from email.mime.text import MIMEText
        from email.mime.multipart import MIMEMultipart

        sender = os.getenv("EMAIL_ADDRESS")
        password = os.getenv("EMAIL_PASSWORD")

        if not sender or not password:
            return False, "❌ EMAIL_ADDRESS or EMAIL_PASSWORD not found in .env file!"

        now = datetime.datetime.now().strftime('%Y-%m-%d %H:%M')

        body = f"""
{'='*60}
  AI BUSINESS ASSISTANT - CHAT SUMMARY
{'='*60}

📅 Date: {now}
🏢 Company: {company_name if company_name else 'N/A'}
💬 Total Messages: {len(st.session_state.messages)}
📄 Documents Uploaded: {st.session_state.docs_uploaded}

{'='*60}
  CONVERSATION
{'='*60}

"""
        for i, m in enumerate(st.session_state.messages, 1):
            role = "👤 YOU" if m['role'] == 'user' else "🤖 AI ASSISTANT"
            body += f"\n{role} (Message {i}):\n{m['content']}\n{'-'*60}\n"

        body += f"""
{'='*60}
  Generated by AI Business Assistant
  Powered by LLaMA 3 | Groq API
{'='*60}
"""

        msg = MIMEMultipart()
        msg['From'] = sender
        msg['To'] = to_email
        msg['Subject'] = f"AI Business Assistant - Chat Summary | {company_name if company_name else 'Report'} | {now}"
        msg.attach(MIMEText(body, 'plain'))

        server = smtplib.SMTP('smtp.gmail.com', 587)
        server.starttls()
        server.login(sender, password)
        server.send_message(msg)
        server.quit()

        return True, "✅ Email sent successfully!"

    except smtplib.SMTPAuthenticationError:
        return False, "❌ Gmail login failed! Check EMAIL_ADDRESS and EMAIL_PASSWORD in .env file. Use App Password, not normal password!"

    except smtplib.SMTPException as e:
        return False, f"❌ SMTP Error: {str(e)}"

    except Exception as e:
        return False, f"❌ Error: {str(e)}"

