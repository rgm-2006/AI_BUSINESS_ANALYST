# extract functions
def extract_pdf_text(file):
    reader = PyPDF2.PdfReader(file)
    text = ""
    for page in reader.pages:
        text += page.extract_text()
    return text

def extract_excel_text(file):
    df = pd.read_excel(file)
    st.session_state.excel_df = df
    return df.to_string()

def export_chat_pdf():
    pdf = FPDF()
    pdf.add_page()
    pdf.set_font("Helvetica", "B", 16)
    pdf.cell(0, 10, "AI Business Assistant - Chat Export", ln=True, align="C")
    pdf.set_font("Helvetica", size=8)
    pdf.cell(0, 8, f"Exported: {datetime.datetime.now().strftime('%Y-%m-%d %H:%M')}", ln=True, align="C")
    pdf.ln(5)
    pdf.set_font("Helvetica", size=10)
    for msg in st.session_state.messages:
        role = "You" if msg["role"] == "user" else "Assistant"
        pdf.set_font("Helvetica", "B", 10)
        pdf.cell(0, 8, f"{role}:", ln=True)
        pdf.set_font("Helvetica", size=9)
        clean_text = msg["content"].encode('latin-1', 'replace').decode('latin-1')
        pdf.multi_cell(0, 6, clean_text)
        pdf.ln(3)
    return pdf.output()

